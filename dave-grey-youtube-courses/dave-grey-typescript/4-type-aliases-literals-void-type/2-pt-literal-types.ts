/* 

- LITERAL TYPES: allows you to set the VALUES that can be assigned to a variable....

  ** syntax 

    let <variable name>: '<string_value_1>' | '<string_value_2>'

      eg: let userName: 'jerry' | 'teddy' 
      
        ** the variable userName can only be assigned values 'jerry' and 'teddy'

        ** you can also add NUMBER as values 

*/

// literal type eg 

let userName: 'jerry' | 'teddy';

userName = 'pinky' // **** see the red underline.... that implies that you cannot add 'pinky' as a value to this variable.... it takes only 'jerry' and 'teddy' as values