// we usually use for (let item of array) and console log item to print items of array one on each line. Instead we can use array method forEach.

// ex

var array = ["jerald", "meera", "sahal", "anu", "arun"]

// to print elements of array using forEach method

array.forEach( n => console.log(n))

// advantage of forEach over map is that map requres extra variable. but for each doesn't require variables
