let num = 100;
console.log(num);

num ++; //adds 1 to the num, so result is 101.
console.log(num);

num --;//subtracts 1 from num, so result is 100.
console.log(num); 

num +=5; //adds 5 to num, so result is 105.
console.log(num);

num -=5; //subtracts 5 from num, so result is 100.
console.log(num);

num /=5; //divides num by 5, so result is 20.
console.log(num);

num %=5 //divides num by 5 and returns the remainder, so the result is 0.
console.log(num);


