/* 
- null and undefined represents NO VALUE
- NULL is set by the developer/ user
- UNDEFINED is set by the javascript when it cannot find value
    - eg: variable without value
    - function without return
    - missing function arguments
    - missing object properties


*/



console.log(20 + null) // 20 **** as null is set by user
console.log(20 + undefined) // NaN ** **as undefined cannot be set by a user