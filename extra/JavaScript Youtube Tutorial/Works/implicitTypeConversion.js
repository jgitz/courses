let num1 = "10"; // 10 is a string.
console.log(num1);
let num2 = "24"; // 24 is a string.
console.log(num2);

let sub = num1 - num2;
console.log(sub);//answer is -14. Result is a number datatype.

let mul = num1 * num2;
console.log(mul);//answer is 240. Result is a number datatype.

let div = num2 / num1;
console.log(div);//answer is 2.4. Result is a number datatype.

let mod = num2 % num1;
console.log(mod);//answer is 4. Result is a number datatype.

// ************IMPORTANT***********

let add = num1 + num2;
console.log(add);// result is 1024!! and not 34. Result is a STRING datatype!!




