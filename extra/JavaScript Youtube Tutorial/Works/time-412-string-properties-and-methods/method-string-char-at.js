/* 
characters of a string is arranged in INDEX numbers STARTING from 0....

.charAt(<index_number>) is used to fetch CHARACTER at that INDEX NUMBER....


*/

let test_string = "12345678"

// to print the FIRST CHARACTER of the string

console.log(test_string.charAt(0))

// to print the last character of the string

console.log(test_string.charAt(test_string.length - 1))