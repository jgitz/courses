/* 
- array.push : add element at the end of the array 

                ** mutates the original

                ** push function returns length of new array as number 

                ** returns the pushed element

- array.unshift: add element to start of the array 

                  ** mutates 
                  ** returns new array length



- array.pop() : remove element from the end

                ** no arguments needed as it removes the last element

                ** mutates the original array

                ** RETURNS THE REMOVED ELEMENT

- array.shift() : remove the first element from
                  the array start 

                  ** no arguments needed 

                  ** mutates the original array

                  ** RETURNS THE REMOVED ELEMENT

- array.indexOf(<element value>) : 

        ** returns the index value if element is present 

        ** returns -1 if the element is not present 
        
- array.includes(<elements>) : 

        ** returns boolean true if the element is present 

        ** returns boolean false if the element is not present 

        ** under the hood the method uses strict equality check i.e type is strictly checked 
        
        ** used to make conditionals in if statement condition.
*/