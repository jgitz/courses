// <>.trim method is used to trim white spaces from START and END of the string 

// <>.trimStart is used to trim white space ONLY from the START of the string 

// <>.trimEnd is used to trim white space ONLY from the END of the string

console.log(" abc ".trim()) // "abc" 

console.log(" abc ".trimStart()) // "abc "

console.log(" abc ".trimEnd()) // " abc"

