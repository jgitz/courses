/* 

- REPEAT STRING METHOD: is used to repeat a string for a NUMBER OF TIMES 

    - syntax: 

        <>.repeat(<n>)

          ** n = number of times you want to repeat the string

*/

console.log("hello".repeat(5)) // hellohellohellohellohello

console.log("hello \n".repeat(5)) /* 

  hello
  hello
  hello
  hello
  hello

*/