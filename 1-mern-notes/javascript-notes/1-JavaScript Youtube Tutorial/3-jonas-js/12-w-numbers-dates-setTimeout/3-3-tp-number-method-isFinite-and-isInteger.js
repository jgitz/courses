/* 

- Number.isFinite(<>) : returns true if an expression/value is FLOATING POINT NUMBER/INTEGER

- Number.isInteger(<>) : returns true if an expression/value is an INTEGER 

*/

console.log(Number.isFinite(23.3)) // true

console.log(Number.isFinite(23)) // true

console.log(Number.isInteger(23.3)) // false

console.log(Number.isInteger(23)) // true


// ***** use Number.isFinite() for checking if an expresion or variable is NUMBER.... and DON'T USE Number.isNaN() for number check